            "• Единицы измерения указаны в подписях к полям (Па, м, кг/м³, с и т.д.).<br>"
            "• При необходимости выберите типовой пресет и затем скорректируйте отдельные параметры вручную.<br><br>"
            "<b>2. Анализ крутильных колебаний</b><br>"
            "• Перейдите на страницу «Крутильные».<br>"
            "• Нажмите кнопку «Выполнить анализ».<br>"
            "• На графике строится кривая D-разбиения σ(p). Красные маркеры показывают пересечения Im(σ)=0, а выделенная точка соответствует критическому пересечению.<br><br>"
            "<b>3. Анализ продольных колебаний</b><br>"
            "• Перейдите на страницу «Продольные».<br>"
            "• Расчёт выполняется в SI-постановке с волновой скоростью a = √(E/ρ).<br>"
            "• После запуска строится параметрическая кривая K₁(ω)–δ(ω) и вычисляются характерные точки.<br><br>"
            "<b>4. Анализ поперечных колебаний</b><br>"
            "• Перейдите на страницу «Поперечные».<br>"
            "• Введите коэффициент внутреннего трения h. Программа автоматически вычислит модальный коэффициент β по формуле β = hγ.<br>"
